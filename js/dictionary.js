export const WORDS = new Set(WORD_LIST);

// 初期単語候補（「ん」終わりは含まない）
export const STARTERS = [
  "りんご", "さくら", "ねこ", "たいよう", "みかん",
  "ひまわり", "うみ", "そら", "やま", "かわ",
  "とり", "はな", "ほし", "くも", "ゆき",
];

/** ランダムな初期単語を返す */
export function randomStarter() {
  return STARTERS[Math.floor(Math.random() * STARTERS.length)];
}

/** 辞書に存在する実在単語か */
export function isRealWord(word) {
  return WORDS.has(word);
}
